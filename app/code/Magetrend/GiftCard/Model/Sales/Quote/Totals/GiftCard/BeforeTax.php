namespace Magetrend\GiftCard\Model\Sales\Quote\Totals\GiftCard;

use \Magetrend\GiftCard\Model\Config\Source\Tax;

class BeforeTax extends \Magetrend\GiftCard\Model\Sales\Quote\Totals\GiftCard
{

    /**
     * Collect address discount amount
     *
     * @param \Magento\Quote\Model\Quote $quote
     * @param \Magento\Quote\Api\Data\ShippingAssignmentInterface $shippingAssignment
     * @param \Magento\Quote\Model\Quote\Address\Total $total
     * @return $this
     * @SuppressWarnings(PHPMD.CyclomaticComplexity)
     */
    public function collect(
        \Magento\Quote\Model\Quote $quote,
        \Magento\Quote\Api\Data\ShippingAssignmentInterface $shippingAssignment,
        \Magento\Quote\Model\Quote\Address\Total $total
    ) {
        if ($this->moduleHelper->getTaxCalculationType($quote->getStoreId()) == Tax::BEFORE) {
            return parent::collect($quote, $shippingAssignment, $total);
        }
        return $this;
    }

    /**
     * Add gift card discount total information to address
     *
     * @param \Magento\Quote\Model\Quote $quote
     * @param \Magento\Quote\Model\Quote\Address\Total $total
     * @return array|null
     * @SuppressWarnings(PHPMD.UnusedFormalParameter)
     */
    public function fetch(\Magento\Quote\Model\Quote $quote, \Magento\Quote\Model\Quote\Address\Total $total)
    {
        if ($this->moduleHelper->getTaxCalculationType($quote->getStoreId()) == Tax::BEFORE) {
            return parent::fetch($quote, $total);
        }

        return null;
    }
}
